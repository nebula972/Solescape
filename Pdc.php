    <p>Solescape est un site web qui propose des produits et des services liés aux voyages. Nous nous engageons à protéger la vie privée de nos utilisateurs. Cette politique de confidentialité explique comment nous collectons, utilisons, divulguons et protégeons les informations personnelles des utilisateurs de Solescape.

    Collecte d'informations
    Nous collectons plusieurs types d'informations lorsque vous utilisez notre site web, notamment :

    Informations personnelles : Nous pouvons collecter des informations telles que votre nom, votre adresse e-mail, votre adresse postale, votre numéro de téléphone et d'autres informations similaires lorsque vous vous inscrivez à notre newsletter, créez un compte, effectuez un achat ou nous contactez.

    Informations de navigation : Nous pouvons collecter des informations sur votre utilisation de notre site web, telles que les pages que vous visitez, le temps passé sur le site et d'autres informations similaires.

    Utilisation des informations
    Nous utilisons les informations que nous collectons pour :

    Fournir des produits et des services : Nous utilisons vos informations personnelles pour traiter vos commandes, répondre à vos demandes et vous fournir des produits et des services.

    Personnaliser l'expérience utilisateur : Nous utilisons les informations que nous collectons pour améliorer votre expérience sur notre site web.

    Communication : Nous utilisons vos informations personnelles pour communiquer avec vous, notamment pour vous informer des promotions, des nouvelles offres et des événements spéciaux.

    Divulgation d'informations
    Nous ne vendons ni ne louons vos informations personnelles à des tiers. Cependant, nous pouvons divulguer vos informations personnelles dans les cas suivants :

    Partenaires de confiance : Nous pouvons partager vos informations personnelles avec des partenaires de confiance qui travaillent avec nous pour fournir des produits et des services à nos utilisateurs.

    Conformité à la loi : Nous pouvons divulguer vos informations personnelles si la loi l'exige ou si nous pensons de bonne foi que cette divulgation est nécessaire pour protéger nos droits, votre sécurité ou celle d'autrui, enquêter sur une fraude ou répondre à une demande du gouvernement.

    Protection des informations
    Nous prenons des mesures de sécurité pour protéger les informations personnelles de nos utilisateurs. Cela inclut des mesures de sécurité techniques et organisationnelles pour empêcher l'accès non autorisé, la divulgation, la perte ou la destruction de vos informations personnelles.

    Modifications de la politique de confidentialité
    Nous pouvons modifier cette politique de confidentialité de temps à autre. Nous vous encourageons à consulter régulièrement cette page pour être informé des changements éventuels.

    Contactez-nous
    Si vous avez des questions sur cette politique de confidentialité, veuillez nous contacter à l'adresse suivante : service@solescape.com.</p>
